import { 
	isIntNumer,
	isEmail,
	isMobile,
} from "@/utils/validate";

export default {
	data() {
		return {
			ruleForm: {},
			flag: '',
			usersFlag: false,
			xiaofeizhexingbieOptions: [],
			jianguanzhexingbieOptions: [],
			shengchanzhexingbieOptions: [],
		};
	},
	mounted() {
		var table = this.$storage.get("sessionTable");
		this.flag = table;
		this.$http({
			url: `${this.$storage.get("sessionTable")}/session`,
			method: "get"
		}).then(({ data }) => {
			if (data && data.code === 0) {
				this.ruleForm = data.data;
			} else {
				this.$message.error(data.msg);
			}
		});
		this.xiaofeizhexingbieOptions = "男,女".split(',')
		this.jianguanzhexingbieOptions = "男,女".split(',')
		this.shengchanzhexingbieOptions = "男,女".split(',')
	},
	methods: {
		xiaofeizhetouxiangUploadChange(fileUrls) {
			this.ruleForm.touxiang = fileUrls;
		},
		jingxiaoshangtouxiangUploadChange(fileUrls) {
			this.ruleForm.touxiang = fileUrls;
		},
		jianguanzhetouxiangUploadChange(fileUrls) {
			this.ruleForm.touxiang = fileUrls;
		},
		shengchanzhetouxiangUploadChange(fileUrls) {
			this.ruleForm.touxiang = fileUrls;
		},
		usersimageUploadChange(fileUrls) {
			this.ruleForm.image = fileUrls;
		},
		onUpdateHandler() {
			if((!this.ruleForm.zhanghao)&& 'xiaofeizhe'==this.flag){
				this.$message.error('账号不能为空');
				return
			}


			if((!this.ruleForm.mima)&& 'xiaofeizhe'==this.flag){
				this.$message.error('密码不能为空');
				return
			}







			if( 'xiaofeizhe' ==this.flag && this.ruleForm.shouji&&(!isMobile(this.ruleForm.shouji))){
				this.$message.error(`手机应输入手机格式`);
				return
			}


			if(this.ruleForm.touxiang!=null) {
				this.ruleForm.touxiang = this.ruleForm.touxiang.replace(new RegExp(this.$base.url,"g"),"");
			}
			if((!this.ruleForm.jingxiaoshanghao)&& 'jingxiaoshang'==this.flag){
				this.$message.error('经销商号不能为空');
				return
			}


			if((!this.ruleForm.mima)&& 'jingxiaoshang'==this.flag){
				this.$message.error('密码不能为空');
				return
			}





			if(this.ruleForm.touxiang!=null) {
				this.ruleForm.touxiang = this.ruleForm.touxiang.replace(new RegExp(this.$base.url,"g"),"");
			}




			if( 'jingxiaoshang' ==this.flag && this.ruleForm.lianxidianhua&&(!isMobile(this.ruleForm.lianxidianhua))){
				this.$message.error(`联系电话应输入手机格式`);
				return
			}


			if( 'jingxiaoshang' ==this.flag && this.ruleForm.youxiang&&(!isEmail(this.ruleForm.youxiang))){
				this.$message.error(`邮箱应输入邮箱格式`);
				return
			}
			if((!this.ruleForm.jianguanzhezhanghao)&& 'jianguanzhe'==this.flag){
				this.$message.error('监管者账号不能为空');
				return
			}


			if((!this.ruleForm.mima)&& 'jianguanzhe'==this.flag){
				this.$message.error('密码不能为空');
				return
			}







			if( 'jianguanzhe' ==this.flag && this.ruleForm.nianling&&(!isIntNumer(this.ruleForm.nianling))){
				this.$message.error(`年龄应输入整数`);
				return
			}


			if(this.ruleForm.touxiang!=null) {
				this.ruleForm.touxiang = this.ruleForm.touxiang.replace(new RegExp(this.$base.url,"g"),"");
			}
			if((!this.ruleForm.shengchanzhehao)&& 'shengchanzhe'==this.flag){
				this.$message.error('生产者号不能为空');
				return
			}


			if((!this.ruleForm.mima)&& 'shengchanzhe'==this.flag){
				this.$message.error('密码不能为空');
				return
			}







			if( 'shengchanzhe' ==this.flag && this.ruleForm.dianhua&&(!isMobile(this.ruleForm.dianhua))){
				this.$message.error(`电话应输入手机格式`);
				return
			}


			if(this.ruleForm.touxiang!=null) {
				this.ruleForm.touxiang = this.ruleForm.touxiang.replace(new RegExp(this.$base.url,"g"),"");
			}
			if('users'==this.flag && this.ruleForm.username.trim().length<1) {
				this.$message.error(`用户名不能为空`);
				return	
			}
			if(this.flag=='users'){
				this.ruleForm.image = this.ruleForm.image.replace(new RegExp(this.$base.url,"g"),"")
			}
			this.$http({
				url: `${this.$storage.get("sessionTable")}/update`,
				method: "post",
				data: this.ruleForm
			}).then(({ data }) => {
				if (data && data.code === 0) {
					this.$message({
						message: "修改信息成功",
						type: "success",
						duration: 1500,
						onClose: () => {
							if(this.flag=='users'){
								this.$storage.set('headportrait',this.ruleForm.image)
							}
						}
					});
				} else {
					this.$message.error(data.msg);
				}
			});
		}
	}
};
